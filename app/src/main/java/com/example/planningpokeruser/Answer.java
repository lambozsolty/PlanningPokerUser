package com.example.planningpokeruser;

public class Answer
{
    private String name;
    private String answer;

    public Answer() {};

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getAnswer()
    {
        return answer;
    }

    public void setAnswer(String answer)
    {
        this.answer = answer;
    }
}
